/**
 * One-shot: self-host the redesign's web fonts.
 *
 * Pulls the variable woff2 files for Schibsted Grotesk (display) and
 * Atkinson Hyperlegible Next (body) from Google Fonts, stores them under
 * public/fonts/<family>/, and writes public/fonts/redesign-fonts.css with the
 * @font-face rules rewritten to local paths. LiebeHeide is copied from the
 * prototype. No runtime dependency on the Google CDN (privacy-first).
 *
 * Run once: `node scripts/fetch-redesign-fonts.mjs`
 */
import { mkdir, writeFile, copyFile } from "node:fs/promises";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const FONTS_DIR = join(ROOT, "public", "fonts");
const UA =
  "Mozilla/5.0 (X11; Linux x86_64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120 Safari/537.36";

const FAMILIES = [
  {
    dir: "schibsted-grotesk",
    css: "https://fonts.googleapis.com/css2?family=Schibsted+Grotesk:ital,wght@0,400..900;1,400..900&display=swap",
  },
  {
    dir: "atkinson-hyperlegible-next",
    css: "https://fonts.googleapis.com/css2?family=Atkinson+Hyperlegible+Next:ital,wght@0,300..800;1,300..800&display=swap",
  },
];

async function get(url, asBuffer = false) {
  const res = await fetch(url, { headers: { "User-Agent": UA } });
  if (!res.ok) throw new Error(`${res.status} ${url}`);
  return asBuffer ? Buffer.from(await res.arrayBuffer()) : res.text();
}

let combinedCss = `/* Self-hosted redesign fonts. Generated by scripts/fetch-redesign-fonts.mjs.\n   Do not edit by hand — re-run the script to regenerate. */\n\n`;

for (const fam of FAMILIES) {
  const outDir = join(FONTS_DIR, fam.dir);
  await mkdir(outDir, { recursive: true });
  let css = await get(fam.css);
  const urls = [...css.matchAll(/url\((https:\/\/fonts\.gstatic\.com\/[^)]+\.woff2)\)/g)].map(
    (m) => m[1],
  );
  let i = 0;
  for (const url of [...new Set(urls)]) {
    const localName = `${fam.dir}-${String(i++).padStart(2, "0")}.woff2`;
    const buf = await get(url, true);
    await writeFile(join(outDir, localName), buf);
    css = css.split(url).join(`/fonts/${fam.dir}/${localName}`);
    console.log(`  ${fam.dir}: ${localName} (${(buf.length / 1024).toFixed(1)} KB)`);
  }
  combinedCss += css.trim() + "\n\n";
}

// LiebeHeide — copy the licensed woff2 from the prototype and add @font-face.
const liebeSrc =
  "/Users/gxjansen/Downloads/Redesign/foundation/LiebeHeideFineliner-Regular.woff2";
await mkdir(join(FONTS_DIR, "liebeheide"), { recursive: true });
await copyFile(liebeSrc, join(FONTS_DIR, "liebeheide", "liebeheide-fineliner-regular.woff2"));
combinedCss += `/* latin */\n@font-face {\n  font-family: 'LiebeHeide';\n  font-style: normal;\n  font-weight: 400;\n  font-display: swap;\n  src: url(/fonts/liebeheide/liebeheide-fineliner-regular.woff2) format('woff2');\n}\n`;

await writeFile(join(FONTS_DIR, "redesign-fonts.css"), combinedCss);
console.log("Wrote public/fonts/redesign-fonts.css");
